package com.maoruiz.EjerciciosUdemy.Seccion4;

public class Ejercicio14Hexadecimal {
    public static void main(String[] args) {

        //decimal
        int b1 = 10;
        System.out.println("b1 = " + b1);

        //hexadecimal
        int b2 = 0xa;
        System.out.println("b2 = " + b2);

        //octal
        int b3 = 012;
        System.out.println("b3 = " + b3);
    }
}

